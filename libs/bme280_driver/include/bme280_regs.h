#define BME280_REG_temp_lsb 0xFB //
#define BME280_REG_temp_xlsb 0xFC // --------------------------
#define BME280_REG_hum_msb 0xFD // Данные влажности (16 бит) (НЕТ для BMP280)
#define BME280_REG_hum_lsb 0xFE//------------------------------
